let score = 80

if (score >= 80)
    console.log('Grade A')
else if (score >= 75 && score < 80)
    console.log('Grade B+')
else if (score >= 70 && score < 75)
    console.log('Grade B')
else if (score >= 65 && score < 70)
    console.log('Grade C+')    
else if (score >= 60 && score < 65)
    console.log('Grade C')
else if (score >= 55 && score < 60)
    console.log('Grade B+')
else if (score >= 50 && score < 55)
    console.log('Grade D')
else
    console.log('Grade E')